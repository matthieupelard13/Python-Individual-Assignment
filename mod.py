def psum(number1, number2):
    """Function to print the sum of two numbers"""
    result_sum = number1 + number2
    print(f"The sum of {number1} and {number2} is: {result_sum}")

def pmultiply(number1, number2):
    """Function to print the product of two numbers"""
    result_multiply = number1 * number2
    print(f"The product of {number1} and {number2} is: {result_multiply}")